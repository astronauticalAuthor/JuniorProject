package project;

public class FieldField {
	String fieldName;
	String fieldType;
	
	public void setName(String fieldName) {
		this.fieldName = fieldName;
	}
	
	public void setType(String fieldType) {
		this.fieldType = fieldType;
	}
	
	public String getName() {
		return fieldName;
	}
	
	public String getType() {
		return fieldType;
	}
}
